// Typewriter
let twPi = 0, twCi = 0, twDeleting = false;
const twEl = document.getElementById('typewriter-text');

function type() {
    const phrases = i18n[currentLang].typewriter;
    if (twPi >= phrases.length) twPi = 0;
    const word = phrases[twPi];
    if (!twDeleting) {
        twEl.textContent = word.slice(0, twCi++);
        if (twCi > word.length) { twDeleting = true; setTimeout(type, 1200); return; }
    } else {
        twEl.textContent = word.slice(0, twCi--);
        if (twCi < 0) { twDeleting = false; twPi = (twPi + 1) % phrases.length; twCi = 0; }
    }
    setTimeout(type, twDeleting ? 45 : 70);
}

// Scroll Reveal
const revealObserver = new IntersectionObserver(entries => {
    entries.forEach(e => {
        if (e.isIntersecting) {
            e.target.classList.add('visible');
            e.target.querySelectorAll('.skill-bar-fill').forEach(bar => {
                bar.style.width = bar.dataset.pct + '%';
            });
        }
    });
}, { threshold: 0.12 });

