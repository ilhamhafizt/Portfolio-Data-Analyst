// URL Flask — sesuaikan jika deploy ke hosting
const API_URL = 'https://NAMA-APP-KAMU.railway.app/api/contact';

async function handleSubmit(e) {
    e.preventDefault();

    const form    = e.target;
    const btn     = document.getElementById('submit-btn');
    const success = document.getElementById('form-success');
    const errorEl = document.getElementById('form-error');
    const t       = i18n[currentLang];

    // Ambil nilai dari input (urutan: nama, email, subjek, pesan)
    const inputs = form.querySelectorAll('input, textarea');
    const nama   = inputs[0].value.trim();
    const email  = inputs[1].value.trim();
    const subjek = inputs[2].value.trim();
    const pesan  = inputs[3].value.trim();

    // Loading state
    btn.textContent = t.form_sending;
    btn.disabled    = true;
    success.classList.add('hidden');
    errorEl.classList.add('hidden');

    try {
        const res  = await fetch(API_URL, {
            method:  'POST',
            headers: { 'Content-Type': 'application/json' },
            body:    JSON.stringify({ nama, email, subjek, pesan })
        });
        const data = await res.json();

        if (res.ok && data.success) {
            // ✅ Sukses
            success.textContent = t.form_success;
            success.classList.remove('hidden');
            btn.textContent = t.form_sent;
            form.reset();
            setTimeout(() => {
                success.classList.add('hidden');
                btn.textContent = t.form_send;
                btn.disabled    = false;
            }, 4000);
        } else {
            // ❌ Error validasi dari server
            errorEl.textContent = data.message || t.form_error_generic;
            errorEl.classList.remove('hidden');
            btn.textContent = t.form_send;
            btn.disabled    = false;
        }

    } catch (_) {
        // ❌ Server tidak terjangkau
        errorEl.textContent = t.form_error_network;
        errorEl.classList.remove('hidden');
        btn.textContent = t.form_send;
        btn.disabled    = false;
    }
}

