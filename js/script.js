// I18N Apply Static Translation + Placeholders
function applyTranslations(lang) {
    const t = i18n[lang];
    // Text / innerHTML elements
    document.querySelectorAll('[data-i18n]').forEach(el => {
        const key = el.getAttribute('data-i18n');
        if (t[key] !== undefined) el.innerHTML = t[key];
    });
    // Placeholder attributes
    document.querySelectorAll('[data-i18n-ph]').forEach(el => {
        const key = el.getAttribute('data-i18n-ph');
        if (t[key] !== undefined) el.setAttribute('placeholder', t[key]);
    });
    // <html> lang attribute
    document.documentElement.lang = lang;
}

// Language Switcher
function setLanguage(lang) {
    currentLang = lang;

    // 1. Static text
    applyTranslations(lang);

    // 2. Dynamic sections
    renderTimeline(lang);
    renderProjects(currentFilter, lang);

    // 3. Re-animate skill bars already visible
    document.querySelectorAll('.reveal.visible .skill-bar-fill').forEach(bar => {
        bar.style.width = bar.dataset.pct + '%';
    });

    // 4. Update toggle button active state (desktop + mobile)
    ['desktop', 'mobile'].forEach(suffix => {
        const idBtn = document.getElementById(`btn-id-${suffix}`);
        const enBtn = document.getElementById(`btn-en-${suffix}`);
        if (!idBtn || !enBtn) return;
        if (lang === 'id') {
            idBtn.classList.add('active');
            enBtn.classList.remove('active');
        } else {
            enBtn.classList.add('active');
            idBtn.classList.remove('active');
        }
    });

    // 5. Reset submit button label to current language
    const btn = document.getElementById('submit-btn');
    if (btn && !btn.disabled) btn.textContent = i18n[lang].form_send;
}

